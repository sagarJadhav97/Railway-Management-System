    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // submit
           if(pass_name.getText().isEmpty() && pass_mobi.getText().isEmpty()){
              JOptionPane.showMessageDialog(this,"Empty filed");
             }
             else{
        try{
             
               String ral_no = pass_name.getText();
               String dep_tr = pass_mobi.getText();
               String arrival_Setarea = pass_alt_no.getText();
               String gender="";
               if(male.isSelected()){
                 gender="Male";
                }else if(female.isSelected()){
                          gender ="Female";
                 }
               
               String arri = pnr_n.getText();
                String ui = e_pass.getText();
               String pas1 = pass_age.getText();
               
               
              
               
          String sql = "INSERT INTO `customer`( `customer_name`, `customer_mobile_no`, `alternative_no`, `gender`, `pnr`, `email`, `age`)"
                  + "VALUES ('"+ral_no+"','"+dep_tr+"','"+arrival_Setarea+"','"+gender+"','"+arri+"','"+ui+"','"+pas1+"')";
          stmt.executeUpdate(sql);
          pass_name.setText("");
          pass_mobi.setText("");
          pass_alt_no.setText("");
          buttonGroup1.clearSelection();
          pnr_n.setText("");
          e_pass.setText("");
          
          pass_age.setText("");
         
          JOptionPane.showMessageDialog(this, "Recored Save Successfully");
        }catch(HeadlessException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());} 
        catch (SQLException ex) {
                        Logger.getLogger(ADD_FLIGHT.class.getName()).log(Level.SEVERE, null, ex);
                    }      }
    }//GEN-LAST:event_jButton1ActionPerformed

