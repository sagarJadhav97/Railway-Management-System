    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // search
           try{

            pst = con.prepareStatement("SELECT  `customer_name`, `customer_mobile_no`, `alternative_no`, `gender`, `pnr`, `email`, `age` FROM `customer`  WHERE pnr='"+onr_ser.getText()+"'");
            rs = pst.executeQuery();

            if(rs.next()==false){
                JOptionPane.showMessageDialog(this, "Recorde Not found");
            }else{
                String fno = String.valueOf(rs.getString("customer_name"));
                String d_flight = String.valueOf(rs.getString("customer_mobile_no"));
                String a_flight = String.valueOf(rs.getString("gender"));
                String d_date = String.valueOf(rs.getString("age"));
             
                pass_nn.setText(fno);
               
                mobile_nnn.setText(d_flight);
                gen_ser.setText(a_flight);
                age_serc.setText(d_date);
                

            }

        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

